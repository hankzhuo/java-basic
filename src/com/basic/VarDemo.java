package com.basic;

public class VarDemo {
    public static void main(String[] args) {
        // 定义两个整型变量
        int a = 0, b = 0;
        System.out.println("a="+a);
        System.out.println("b="+b);
        // 定义一个汉字
        char ch = '中';
        System.out.print("ch="+ '\t' + ch + "\n");
        // 用科学计数法表示浮点型数据
        double d = 1.24E5;
        float f = 1.23e5f;
        double d1 = .2;
        System.out.println(d);
        System.out.println(f);
        System.out.println(d1);
    }
}
