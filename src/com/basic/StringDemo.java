package com.basic;

public class StringDemo {
    public static void main(String[] args) {
        // 定义一个空字符串
        String s1 = "";
        System.out.println("s1="+s1);
        // 定义一个字符串，内容是 Hello
        String s2 = "Hello";
        System.out.println("s2="+s2);
        // 定义一个包含 unicode 字符的字符串
        String s3 = "A\u005d\u005fB";
        System.out.println("s3="+s3);
    }
}
